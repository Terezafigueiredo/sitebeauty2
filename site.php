<script>
  document.addEventListener('DOMContentLoaded', () => {

    /* ===== CARROSSEL ===== */
    const imagens = Array.from(document.querySelectorAll('.carrossel-slide img'));
    const btnProximo = document.querySelector('.btn.proximo');
    const btnAnterior = document.querySelector('.btn.anterior');
    let index = 0;
    let intervalo;

    function mostrarImagemAtual() {
      if (!imagens.length) return;
      imagens.forEach((img, i) => {
        img.classList.remove('active');
        if (i === index) {
          img.classList.add('active');
        }
        img.setAttribute('aria-hidden', i === index ? 'false' : 'true');
      });
    }

    function proximaImagem() {
      index = (index + 1) % imagens.length;
      mostrarImagemAtual();
    }

    function imagemAnterior() {
      index = (index - 1 + imagens.length) % imagens.length;
      mostrarImagemAtual();
    }

    function iniciarAutoPlay() {
      pararAutoPlay();
      intervalo = setInterval(proximaImagem, 4000);
    }

    function pararAutoPlay() {
      if (intervalo) clearInterval(intervalo);
    }

    if (btnProximo && btnAnterior && imagens.length) {
      btnProximo.addEventListener('click', () => {
        proximaImagem();
        pararAutoPlay();
        iniciarAutoPlay();
      });

      btnAnterior.addEventListener('click', () => {
        imagemAnterior();
        pararAutoPlay();
        iniciarAutoPlay();
      });

      document.addEventListener('keydown', (e) => {
        if (e.key === 'ArrowRight') {
          proximaImagem();
          pararAutoPlay();
          iniciarAutoPlay();
        } else if (e.key === 'ArrowLeft') {
          imagemAnterior();
          pararAutoPlay();
          iniciarAutoPlay();
        }
      });

      const firstActive = imagens.findIndex(img => img.classList.contains('active'));
      if (firstActive > -1) index = firstActive;

      mostrarImagemAtual();
      iniciarAutoPlay();
    }


    /* ===== SAIBA MAIS ===== */
    const botoesSaibaMais = Array.from(document.querySelectorAll('.saiba-mais'));

    botoesSaibaMais.forEach((botao) => {
      botao.addEventListener('click', () => {
        const card = botao.closest('.card');
        if (!card) return;
        const pacotes = card.querySelector('.pacotes');
        const jaExpandido = card.classList.contains('expandido');

        document.querySelectorAll('.card').forEach(c => {
          c.classList.remove('expandido');
          const p = c.querySelector('.pacotes');
          if (p) p.classList.remove('ativo');
          const b = c.querySelector('.saiba-mais');
          if (b) b.classList.remove('ativo'), b.setAttribute('aria-expanded', 'false');
        });

        if (!jaExpandido) {
          card.classList.add('expandido');
          if (pacotes) pacotes.classList.add('ativo');
          botao.classList.add('ativo');
          botao.setAttribute('aria-expanded', 'true');

          setTimeout(() => {
            card.scrollIntoView({ behavior: 'smooth', block: 'center' });
          }, 120);
        }
      });
    });


    /* ===== REMOVIDO O BLOQUEIO DO FORMULÁRIO ===== */
    // Nada aqui interfere no envio!
    // O formulário agora enviará normalmente para processa_agendamento.php


    /* ===== FALLBACK PARA IMAGENS ===== */
    imagens.forEach(img => {
      img.addEventListener('error', () => {
        img.src = 'Assets/img/placeholder.png';
        img.alt = 'Imagem indisponível';
        img.classList.add('active');
      });
    });

  });
</script>


